// Hook para usar traduções
export const useTranslation = (language = 'pt') => {
  const t = (key) => {
    const keys = key.split('.')
    let value = translations[language]
    
    for (const k of keys) {
      value = value?.[k]
    }
    
    return value || key
  }
  
  return { t }
}
